function writeTextFile(relPath: string, contents: string): void {
  const abs = path.join(ROOT, relPath);
  mkdirSync(path.dirname(abs), { recursive: true });
  writeFileSync(abs, contents, "utf8");
}

function addManifest(
  id: string,
  relPath: string,
  details: Partial<Pick<ManifestEntry, "license" | "source" | "originality" | "date">> = {},
): void {
  manifest.push({
    id,
    src: "/" + relPath,
    path: relPath,
    kind: "image",
    license: details.license ?? "CC0",
    source: details.source ?? SOURCE,
    originality: details.originality ?? ORIGINALITY,
    date: details.date ?? GENERATED_DATE,
  });
}

