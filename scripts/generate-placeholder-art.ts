function carBodyPath(car: CarSheet): string {
  const noseLeft = car.nose === "wide" ? -30 : car.nose === "blunt" ? -27 : -24;
  const noseRight = car.nose === "wide" ? 30 : car.nose === "blunt" ? 27 : 24;
  const frontY = car.nose === "wedge" ? 13 : 12;
  const shoulder = car.stance === "wide" ? 29 : car.stance === "narrow" ? 24 : 27;
  const roof = car.tail === "long" ? 17 : car.tail === "wide" ? 15 : 12;
  const cabinY = car.tail === "fastback" ? -15 : -14;
  const tailLeft = car.tail === "wide" ? -28 : car.tail === "long" ? -24 : -21;
  const tailRight = car.tail === "wide" ? 28 : car.tail === "long" ? 24 : 21;

  if (car.nose === "wedge") {
    return `M${-shoulder} ${frontY} L${shoulder} ${frontY} L${tailRight} -5 L${roof} ${cabinY} L${-roof} ${cabinY} L${tailLeft} -5 Z`;
  }

  return `M${noseLeft} ${frontY} L${noseRight} ${frontY} L${tailRight} -7 L${roof} ${cabinY} L${-roof} ${cabinY} L${tailLeft} -7 Z`;
}

